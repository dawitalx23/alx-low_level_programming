/**
 * op_add - return sum of ints
 * @a: arg1
 * @b: arg2
 *
 * Return: integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_mul - multiplies two nums
 * @a: arg1
 * @b: arg2
 *
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_sub - subtract b from a
 * @a: arg1
 * @b: arg2
 *
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - diveds b by a
 * @a: arg1
 * @b: arg2
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modular
 * @a: arg1
 * @b: arg2
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
